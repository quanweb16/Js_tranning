// Ví dụ về vòng lặp trong JavaScript
for (let i = 0; i < 5; i++) {
    console.log(i); // 0 1 2 3 4
  }
  
  let myArray = [10, 20, 30, 40, 50];
  for (let i = 0; i < myArray.length; i++) {
    console.log(myArray[i]); // 10 20 30 40 50
  }
  
  let i = 0;
  while (i < 3) {
    console.log(i); // 0 1 2
    i++;
  }