// Ví dụ về câu lệnh điều kiện trong JavaScript
let age = 18;
if (age >= 18) {
  console.log("You are an adult.");
} else {
  console.log("You are a minor.");
}

let weather = "sunny";
if (weather === "sunny") {
  console.log("It's a great day to go outside!");
} else if (weather === "rainy") {
  console.log("It's a good day to stay indoors.");
} else {
  console.log("I'm not sure what the weather is like.");
}